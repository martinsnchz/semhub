    <br/>
    <br/>
    Les frères Orville Wright (19 août 1871 - 30 janvier 1948) et Wilbur Wright (16 avril 1867 - 30 mai 1912) sont deux célèbres pionniers américains de l'aviation, à la fois chercheurs1, ingénieurs, concepteurs, constructeurs et pilotes.
<br/>
Après des vols de mise au point sur planeurs, ils affirment avoir effectué en 1903 leur premier vol motorisé. Ils se sont distingués de leurs prédécesseurs et de leurs contemporains par leur approche analytique et expérimentale du problème. Leur contribution essentielle sera d'avoir correctement analysé la mécanique de vol du virage et d'avoir réalisé, en 1902, les premiers vols contrôlés grâce au couplage de la gouverne de direction et du gauchissement (obtenu par vrillage) des ailes. Maîtrisant le pilotage, ils effectuent en 1905 les premiers vols pouvant être qualifiés de « stables », de longue durée, avec des virages inclinés et non dérapés. Cependant, leur obsession du secret autour de leurs machines et de leurs capacités à réaliser un vol motorisé contrôlé (qu'ils maintiendront presque totalement jusqu'à l'obtention de brevet d'invention en 1905) entraînera un scepticisme général, en particulier en Europe, quand ils commenceront à communiquer en 1905 tout en exigeant un contrat commercial ferme avant toute démonstration. Cela explique le décalage de plusieurs années entre les premiers vols contrôlés de 1905, à l'écart de tout spectateur dans les dunes de Caroline du Nord, et les vols publics de 1908 où leur maîtrise du pilotage sera reconnue. Consacrant leur énergie à protéger leur invention et à des luttes de brevets, ils ne remettent pas en cause la configuration atypique de leur machine (configuration canard, pas d'ailerons, pas de roues, hélices à l'arrière), qui est obsolète en 1910 et ne sera pas poursuivie. <br/><br/><br/>
